def get_color_name(r: int, g: int, b: int) -> str:
    """
    Returns the human-readable name of the nearest color to the given RGB.
    Uses Euclidean distance in 3D RGB space.
    """
    min_dist = float("inf")
    nearest = "Unknown"
    for name, (pr, pg, pb) in COLOR_PALETTE.items():
        dist = math.sqrt((r - pr) ** 2 + (g - pg) ** 2 + (b - pb) ** 2)
        if dist < min_dist:
            min_dist = dist
            nearest = name
    return nearest
